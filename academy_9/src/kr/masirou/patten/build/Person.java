package kr.masirou.patten.build;

public class Person {
    private String schoolName;
    private String name;
    private int age;
    private int height;
    private int weight;
    Person(String name, String schoolName, int age, int height, int weight) {
        this.name = name;
        this.schoolName = schoolName;
        this.age = age;
        this.height = height;
        this.weight = weight;

    }
}
